async def perse_user_info(request : Request) -> UserCreate:
    content_type = request.headers.get("content_type","")
    
    if "application/json" in content_type:
        data = await request.json()
    
    elif "form" in content_type:
        form = await request.form()
        data = dict(form)
    else:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Content type not supported"
        )       
    
    return UserCreate(**data)   

    '''
    (**data) unpacks the dictionary into keyword arguments for the UserCreate Pydantic model.
    For example, if data = {'name': 'Alice', 'email': 'alice@example  com'},
    then UserCreate(**data) is equivalent to UserCreate(name='Alice', email= 'alice@example.com') 
    '''
    

