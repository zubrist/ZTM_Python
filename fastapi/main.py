# @asynccontextmanager
async def lifespan(app: FastAPI):
    '''
    lifespan(app: FastAPI): This is an asynchronous context manager function that takes the FastAPI app as 
    an argument. 
    Inside this function, we can define code that will run when the application starts and when it shuts down.
    
    IMPORTANT: The models are already imported at the top of this file (from models import *)
    So they're registered with Tortoise by the time this lifespan runs.
    '''
    logger.info("="*60)
    logger.info(" 🚀 Starting Application ...")
    logger.info("="*60)
    
    try:
        logger.info("📦 Initializing Tortoise ORM...")
        
        # Initialize Tortoise with config
        # The config.py has the database path and model definitions
        await Tortoise.init(config=TORTOISE_ORM)
        
        logger.info("✅ Tortoise initialized")
        logger.info("🏗️  Creating database tables from models...")
        
        # Create all tables based on models
        await Tortoise.generate_schemas()
        
        logger.info("✅ Database tables created successfully")
        
    except Exception as e:
        logger.error(f"❌ Failed to initialize database: {e}")
        raise
    
    logger.info("=" * 60)
    logger.info("✅ DATABASE READY - App is running!")
    logger.info("=" * 60)
    
    yield  # App runs here - requests are processed
    
    # ========== SHUTDOWN CODE ==========
    logger.info("=" * 60)
    logger.info("🛑 SHUTTING DOWN APPLICATION")
    logger.info("=" * 60)
    
    try:
        logger.info("🔌 Closing database connections...")
        await Tortoise.close_connections()
        logger.info("✅ Database closed")
    except Exception as e:
        logger.error(f"❌ Error closing database: {e}")
    
    logger.info("=" * 60)
    logger.info("✅ APPLICATION STOPPED")
    logger.info("=" * 60)

    
    
    
    
    


