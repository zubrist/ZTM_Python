# Custom Request/Response Logging Middleware
# This middleware logs timing information for every request
from starlette.middleware.base import BaseHTTPMiddleware
from starlette.requests import Request as StarletteRequest
from starlette.responses import Response as StarletteResponse

class RequestLoggingMiddleware(BaseHTTPMiddleware):
    '''
    This middleware logs incoming requests and outgoing responses.
    It measures how long each request takes to process.
    
    INTERVIEW QUESTION: "How would you implement request logging without breaking streaming responses?"
    ANSWER: Use raw ASGI middleware instead of BaseHTTPMiddleware to avoid buffering.
    BaseHTTPMiddleware is easier to understand but slower and buffers the whole response.
    '''
    
    async def dispatch(self, request: StarletteRequest, call_next):
        # Before the request is processed
        start_time = get_time()
        
        # Log the incoming request
        logger.info(f"→ {request.method} {request.url.path} from {request.client.host}")
        
        # Process the request (run the actual endpoint)
        response = await call_next(request)
        
        # After the response is ready
        process_time = get_time() - start_time
        
        # Log the response with timing information
        logger.info(f"← {request.method} {request.url.path} - Status {response.status_code} - {process_time:.3f}s")
        
        # Add custom header showing how long the request took
        response.headers["X-Process-Time"] = str(process_time)
        
        return response

# Add the logging middleware to the app
app.add_middleware(RequestLoggingMiddleware)
'''
Middleware order matters!
Middleware is executed in REVERSE order of how you add it.
So the LAST middleware added (RequestLoggingMiddleware) runs FIRST on incoming requests.
This means logs will show timing including CORS processing.

Order of execution on incoming request:
1. RequestLoggingMiddleware.dispatch() - START
2. CORSMiddleware - runs
3. Your endpoint code - runs
4. Response returned
5. RequestLoggingMiddleware.dispatch() - FINISH (logs and adds header)
'''


